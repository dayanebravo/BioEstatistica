### MEDIA
idades <- c(25, 30, 35, 40, 45)  
media <- mean(idades)  
print(media)


idades_outlier <- c(25, 30, 35, 40, 45, 100)  
media_outlier <- mean(idades_outlier)  
print(media_outlier)  





### MEDIANA
idades <- c(25, 30, 35, 40, 45)  
mediana <- median(idades)  
print(mediana) 


idades_outlier <- c(25, 30, 35, 40, 45, 100)  
mediana_outlier <- median(idades_outlier)  
print(mediana_outlier)  







